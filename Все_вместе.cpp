    // Функция формирует строку с результатом сравнения двух чисел
std::string makeDecision(int x, int y) {
    if (x > y) {
        return std::to_string(x) + " >" + std::to_string(y);
    }
    else if (x < y) {
        return std::to_string(x) + "< " + std::to_string(y);
    }
    else {
        return std::to_string(x) + "==" + std::to_string(y);
    }
}


