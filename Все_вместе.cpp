// результат: 0
// Пример 2:
// x=8 y=2
// результат: 4


// Функция безопасного деления
double safeDiv(int x, int y) {
    if (y == 0) {
        return 0;  // защита от деления на 0
    }
    return static_cast<double>(x) / y;  // обычное деление
}


